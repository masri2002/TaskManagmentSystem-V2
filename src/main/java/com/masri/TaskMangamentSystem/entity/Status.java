package com.masri.TaskMangamentSystem.entity;

/**
 * Enum representing the status of a task.
 */
public enum Status {
    /**
     * Task is pending and has not been started.
     */
    PENDING,

    /**
     * Task is in progress and actively being worked on.
     */
    IN_PROGRESS,

    /**
     * Task is completed and finished.
     */
    COMPLETED
}
